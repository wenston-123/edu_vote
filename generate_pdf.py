def get_major_intro_key(school_name, major_name):
    """生成专业介绍的匹配键"""
    # 精确匹配
    key = f"{school_name}|{major_name}"
    if key in MAJOR_INTRODUCTIONS:
        return key
    # 模糊匹配：用学校名+专业关键词
    for intro_key in MAJOR_INTRODUCTIONS:
        intro_school, intro_major = intro_key.split("|", 1)
        if intro_school == school_name:
            # 尝试部分匹配
            intro_words = re.split(r"[（）()、，\s]+", intro_major)
            for w in intro_words:
                if len(w) >= 3 and w in major_name:
                    return intro_key
    return None


