def main():
    import argparse
    parser = argparse.ArgumentParser(
        description="高考录取数据 PDF 报告生成器",
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog="""
示例:
  python3 generate_pdf.py \\
    --province 山西 --category 物理类 --year 2025 \\
    --rank-min 4000 --rank-max 6000 \\
    --highlight-min 4600 --highlight-max 5300

  python3 generate_pdf.py \\
    --province 广东 --category 历史类 --year 2025 \\
    --rank-min 2000 --rank-max 5000 \\
    --highlight-min 3000 --highlight-max 4000 \\
    --output 广东历史类报告.pdf
        """,
    )
    parser.add_argument("--province", required=True, help="省份，如：山西")
    parser.add_argument("--category", required=True, help="科类，如：物理类、历史类")
    parser.add_argument("--year", type=int, required=True, help="年份，如：2025")
    parser.add_argument("--rank-min", type=int, required=True, help="位次区间下限，如：4000")
    parser.add_argument("--rank-max", type=int, required=True, help="位次区间上限，如：6000")
    parser.add_argument("--highlight-min", type=int, required=True, help="重点位次区间下限，如：4600")
    parser.add_argument("--highlight-max", type=int, required=True, help="重点位次区间上限，如：5300")
    parser.add_argument("--output", help="输出 PDF 文件路径（默认 output/{省份}{年份}{科类}{位次区间}名录取报告.pdf）")
    args = parser.parse_args()

    # 年份校验
    if args.year > 2026:
        print(f"❌ 错误：年份不能超过 2026 年（当前输入：{args.year}）")
        sys.exit(1)
    if args.year > 2025:
        print(f"⚠️  提示：当前数据仅支持到 2025 年，{args.year} 年的录取数据尚未发布。")

    # 生成输出路径
    if args.output:
        output_path = args.output
    else:
        os.makedirs(OUTPUT_DIR, exist_ok=True)
        output_path = os.path.join(
            OUTPUT_DIR,
            f"{args.province}{args.year}{args.category}{args.rank_min}-{args.rank_max}名录取报告.pdf"
        )

    print(f"📊 正在生成 PDF 报告...")
    print(f"   省份: {args.province} | 科类: {args.category} | 年份: {args.year}")
    print(f"   位次区间: {args.rank_min}-{args.rank_max} | 重点区间: {args.highlight_min}-{args.highlight_max}")

    doc = SimpleDocTemplate(
        output_path,
        pagesize=A4,
        leftMargin=18*mm,
        rightMargin=18*mm,
        topMargin=15*mm,
        bottomMargin=15*mm,
        title=f"{args.province}{args.year}{args.category}{args.rank_min}-{args.rank_max}名录取报告",
        author="gaokao-report",
    )

    story = build_story(
        province=args.province,
        category=args.category,
        year=args.year,
        rank_min=args.rank_min,
        rank_max=args.rank_max,
        highlight_min=args.highlight_min,
        highlight_max=args.highlight_max,
    )
    doc.build(story)
    print(f"✅ PDF 报告已生成：{output_path}")
    print(f"   文件大小：{os.path.getsize(output_path) / 1024:.1f} KB")


if __name__ == "__main__":
    main()
